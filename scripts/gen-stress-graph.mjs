#!/usr/bin/env node
// Generates a large stress-test graph (773 nodes / 1642 edges) using the
// built-in `primitives` pack vocabulary, to reproduce the performance problems
// seen on a real-world graph of that size. Deterministic via a seeded PRNG so
// the output is stable across runs.
//
//   node scripts/gen-stress-graph.mjs
//
// Writes stress-773.graph.json into the client-next public canvases dir and a
// copy under .canvases.

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');

const NODE_COUNT = 773;
const EDGE_COUNT = 1642;
const ROOT_COUNT = 40; // top-level modules

// --- deterministic PRNG (mulberry32) ---
function mulberry32(seed) {
  let a = seed >>> 0;
  return () => {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
const rand = mulberry32(0x1ee7c0de);
const pick = (arr) => arr[Math.floor(rand() * arr.length)];
const randInt = (n) => Math.floor(rand() * n);

const MODULE_COLOR = '#4a90e2';
const SUBMODULE_COLOR = '#e2a874';
const CONCEPT_COLOR = '#7ab06d';

const ARROW_LABELS = ['depends on', 'calls', 'feeds', 'writes', 'reads', 'resolves against', 'emits', 'subscribes to'];
const RELATES_LABELS = ['shares config', 'warms', 'configured by', 'related to', 'mirrors', 'pairs with'];

// --- build nodes as a forest: roots -> submodules -> leaf boxes ---
const nodes = [];
const childrenOf = []; // index -> parent index or -1
const parentOf = [];

for (let i = 0; i < ROOT_COUNT; i++) {
  const idx = nodes.length;
  nodes.push({
    id: `mod-${i}`,
    kind: 'prim.box',
    props: {
      label: `Module ${i}`,
      tag: 'module',
      color: MODULE_COLOR,
      description: `Top-level module ${i}.`,
    },
    tags: [],
  });
  parentOf[idx] = -1;
}

// Remaining nodes attach to an existing node, building depth.
let counter = 0;
while (nodes.length < NODE_COUNT) {
  const parentIdx = randInt(nodes.length);
  const depth = depthOf(parentIdx);
  const isConcept = depth >= 2 && rand() < 0.4;
  const idx = nodes.length;
  nodes.push({
    id: `n-${counter}`,
    kind: 'prim.box',
    props: {
      label: isConcept ? `Concept ${counter}` : `Unit ${counter}`,
      tag: isConcept ? 'concept' : 'submodule',
      color: isConcept ? CONCEPT_COLOR : SUBMODULE_COLOR,
      description: `Generated ${isConcept ? 'concept' : 'submodule'} ${counter} under ${nodes[parentIdx].id}.`,
    },
    tags: [],
  });
  parentOf[idx] = parentIdx;
  counter++;
}

function depthOf(idx) {
  let d = 0;
  let cur = idx;
  while (parentOf[cur] !== undefined && parentOf[cur] !== -1) {
    cur = parentOf[cur];
    d++;
    if (d > 50) break;
  }
  return d;
}

// --- edges ---
const edges = [];
const seen = new Set();
const edgeKey = (from, to) => `${from}->${to}`;

// 1. containment edges for every non-root node
for (let i = 0; i < nodes.length; i++) {
  const p = parentOf[i];
  if (p === -1 || p === undefined) continue;
  const from = nodes[i].id;
  const to = nodes[p].id;
  edges.push({
    id: `contains.${from}`,
    kind: 'prim.contains',
    from,
    to,
    props: {},
    tags: [],
  });
  seen.add(edgeKey(from, to));
}

// 2. fill the rest with arrows + relates between random distinct nodes
let extra = 0;
while (edges.length < EDGE_COUNT) {
  const a = randInt(nodes.length);
  let b = randInt(nodes.length);
  if (a === b) continue;
  const from = nodes[a].id;
  const to = nodes[b].id;
  if (seen.has(edgeKey(from, to))) continue;
  seen.add(edgeKey(from, to));
  const isArrow = rand() < 0.55;
  edges.push({
    id: `x.${extra}`,
    kind: isArrow ? 'prim.arrow' : 'prim.relates',
    from,
    to,
    props: { label: isArrow ? pick(ARROW_LABELS) : pick(RELATES_LABELS) },
    tags: [],
  });
  extra++;
}

const graph = {
  version: 3,
  pack: 'primitives',
  info: `# Stress Test (${NODE_COUNT} nodes / ${EDGE_COUNT} edges)\n\nSynthetic graph replicating the size of a real-world canvas that performs unusably. Generated by \`scripts/gen-stress-graph.mjs\` for performance testing.`,
  nodes,
  edges,
  defaultView: 'architecture',
};

const json = JSON.stringify(graph, null, 0).replace(/},{/g, '},\n    {');

const targets = [
  join(repoRoot, 'packages/client-next/public/canvases/stress-773.graph.json'),
  join(repoRoot, '.canvases/stress-773.graph.json'),
];
for (const t of targets) {
  writeFileSync(t, JSON.stringify(graph, null, 2) + '\n');
  console.log(`wrote ${t}`);
}
console.log(`nodes: ${nodes.length}, edges: ${edges.length}`);
console.log(`  contains: ${edges.filter((e) => e.kind === 'prim.contains').length}`);
console.log(`  arrow:    ${edges.filter((e) => e.kind === 'prim.arrow').length}`);
console.log(`  relates:  ${edges.filter((e) => e.kind === 'prim.relates').length}`);
